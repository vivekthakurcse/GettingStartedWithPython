# Those Operators which is used to compare or check two or more statement.
# Logical Operators : and , or & not
# Let 
a , b = 5 , 4
# and operator : gives true when both of the statement are true
print(a>4 and b<10)
# or operator : gives true when any of them statement is true. If both of them is false then it gives false
print(a>4 or b>10)
print(a<4 or b>10)
# not operator : it gives reverse result. Meanwhile TRUE -> FALSE and FALSE -> TRUE
print(not(a))
